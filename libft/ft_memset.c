#include "libft.h"

void				*ft_memset(void *s, int c, size_t size)
{
	size_t			i;
	unsigned char	*s_point;

	i = 0;
	s_point = (unsigned char *)s;
	while (i < size)
		s_point[i++] = c;
	return (s);
}
